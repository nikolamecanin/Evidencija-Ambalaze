    private void tblZbirnoStanjeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblZbirnoStanjeKeyPressed
       tblZbirnoStanje.requestFocus();
       if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           int selectedRow = tblZbirnoStanje.getSelectedRow();
           if(selectedRow != -1){
               Long id_ambalaze = (Long) tblZbirnoStanje.getValueAt(selectedRow, 0);
               String naziv_ambalaza = (String) tblZbirnoStanje.getValueAt(selectedRow, 1);
               
               
               ZaduzenjaPoAmbalazi dialog = new ZaduzenjaPoAmbalazi(this, true,id_ambalaze,naziv_ambalaza);
               dialog.setTxtNazivAmbalaze(naziv_ambalaza);
               dialog.setTxtSifra(id_ambalaze);
               this.dispose();
               
               
           dialog.setLocationRelativeTo(null);
           dialog.setVisible(true);
           }
           
           
       }
    }//GEN-LAST:event_tblZbirnoStanjeKeyPressed

