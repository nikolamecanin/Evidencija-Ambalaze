    private void prepareView(){
        popuniTabelu();
        postaviDatum();
    }
    private void popuniTabelu(){
        List<ZbirnoStanje> zb = new ArrayList<>();
        DatabaseBroker d=  new DatabaseBroker();
        zb = d.ZbirnoStanje();
        ZbirnoStanjeTableModel zstm = new ZbirnoStanjeTableModel(zb);
        tblZbirnoStanje.setModel(zstm);
    }
    
    private void postaviDatum(){
        LocalDate datum = LocalDate.now();
        String datumS = datum.format(DateTimeFormatter.ofPattern("dd-MM-YYYY"));
        txtDatum.setText(datumS);
        txtDatum.setEditable(false);
    }
