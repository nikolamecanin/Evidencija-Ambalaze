    private void prepareView(){
        fillAmbalaze();
        popuniTabelu();
        
    }
    
    
    private void fillAmbalaze(){
        List<Ambalaza> ambalaze = new ArrayList<>();
        ambalaze = dbbr.vratiAmbalaze();
        for(Ambalaza a: ambalaze){
            comboAmbalaze.addItem(a);
        }
    }
    
    private void popuniTabelu(){
        List<Zaduzenje> zaduzenja = new ArrayList<>();
        
        zaduzenja = dbbr.vratiZaduzenja(getTxtSifra());
        
        ZaduzenjaTableModel ztm = new ZaduzenjaTableModel(zaduzenja);
        tblZaduzenja.setModel(ztm);
        
       
    }

    

    
   
