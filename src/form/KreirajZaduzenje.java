    private void btnZaduziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZaduziActionPerformed
        String kupac_idS = txtSifra.getText();
        Long kupac_id = Long.parseLong(kupac_idS);
        
        String kolicinaText = txtKolicina.getText().trim();
        String napomena = txtNapomena.getText();
         if (kolicinaText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Unesite količinu!", "Greška", JOptionPane.ERROR_MESSAGE);
    return;
         }
        if (!kolicinaText.matches("-?\\d+")){
            JOptionPane.showMessageDialog(this, "U polju kolicina moze biti prosledjen samo broj", "Error", JOptionPane.ERROR_MESSAGE);
            
            return;
        }
       

        int kolicina = Integer.parseInt(txtKolicina.getText());
        if(kolicina == 0){
            JOptionPane.showMessageDialog(this, "Ne moze se zaduziti 0 kolicina", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
        
        Ambalaza ambalaza = (Ambalaza) comboAmbalaze.getSelectedItem();
        if(ambalaza == null){
            JOptionPane.showMessageDialog(this, "Nije izabrana ambalaza", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }else{
            int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da zaduzite kupca: "+txtImePrezime.getText()+ "\n ambalaza: "+comboAmbalaze.getSelectedItem()+"\n kolicina: "+txtKolicina.getText()+"\nUz napomenu: "+napomena, "Confirm", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.NO_OPTION){
             return;   
            }
            if(result == JOptionPane.CLOSED_OPTION){
                return;
            }
            else{
        dbbr.ZaduziKupca(kupac_id, ambalaza.getId(), LocalDateTime.now(), kolicina,napomena);
        JOptionPane.showMessageDialog(this, "Zaduznje uspesno izvrseno");
        }
        }
        popuniTabelu();
        txtKolicina.setText("");
    }//GEN-LAST:event_btnZaduziActionPerformed

    private void btnRazduziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRazduziActionPerformed
        int selectedRow = tblZaduzenja.getSelectedRow();
        if (selectedRow == -1){
            JOptionPane.showMessageDialog(this, "Nista nije selektovano u tabeli", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        else if(tblZaduzenja.getSelectedRowCount() > 1){
            JOptionPane.showMessageDialog(this, "Moguce je selektovati najvise jedan red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        else{
         Long id = (Long) tblZaduzenja.getValueAt(selectedRow, 0);
         int kolicina = (int) tblZaduzenja.getValueAt(selectedRow, 3);
        
          
        String ime_prezime = (String) tblZaduzenja.getValueAt(selectedRow, 1);
        String naziv_ambalaze = (String) tblZaduzenja.getValueAt(selectedRow, 2);
        int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da razduzite kupca: "+ime_prezime+"\n ambalaza: "+naziv_ambalaze+"\n kolicina: "+kolicina+" ?", "Brisanje zaduzenja", JOptionPane.YES_NO_OPTION);
        if(odgovor == JOptionPane.NO_OPTION){
            return;
        }
        if(odgovor == JOptionPane.CLOSED_OPTION){
                return;
            }
        else{
            dbbr.ObrisiZaduzenje(id);
        JOptionPane.showMessageDialog(this, "Uspesno obrisano zaduzenje");
        }
        
        popuniTabelu();
        }
       
        
        
    }//GEN-LAST:event_btnRazduziActionPerformed

