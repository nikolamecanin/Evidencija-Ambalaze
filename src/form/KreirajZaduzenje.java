    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String kolicinaText = txtKolicina.getText().trim();
        String napomena = txtNapomena.getText();
         if (kolicinaText.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Unesite količinu!", "Greška", JOptionPane.ERROR_MESSAGE);
    return;
         }
        if (!kolicinaText.matches("-?\\d+")){
            JOptionPane.showMessageDialog(this, "U polju kolicina moze biti prosledjen samo broj", "Error", JOptionPane.ERROR_MESSAGE);
            
            return;
        }
       

        int kolicina = Integer.parseInt(txtKolicina.getText());
        if(kolicina == 0){
            JOptionPane.showMessageDialog(this, "Ne moze se razduziti za 0", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        int selected_row = tblZaduzenja.getSelectedRow();
        if(selected_row == - 1){
            JOptionPane.showMessageDialog(this, "Nista nije selektovano u tabeli", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
        else if (tblZaduzenja.getSelectedRowCount() > 1){
            JOptionPane.showMessageDialog(this, "Moguce je selektovati najvise jedan red", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            Long id_zaduzenja = (Long) tblZaduzenja.getValueAt(selected_row, 0);
            String ime_prezime = (String) tblZaduzenja.getValueAt(selected_row, 1);
            String naziv_ambalaze = (String) tblZaduzenja.getValueAt(selected_row, 2);
            
            int odgovor = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da prepravite zaduzenje: id "+id_zaduzenja+"\nAmbalaza: "+naziv_ambalaze+"\nIme i Prezime: "+ime_prezime+"\n Umanjiti za kolicinu: "+kolicina+"\nUz napomenu: "+napomena);
            if (odgovor == JOptionPane.NO_OPTION){
                return;
            }
            if (odgovor == JOptionPane.CANCEL_OPTION){
                return;
            }
            if(odgovor == JOptionPane.CLOSED_OPTION){
                return;
            }
            else{
            dbbr.izmeniZaduzenje(id_zaduzenja, kolicina,napomena);
            JOptionPane.showMessageDialog(this, "Uspesno izvrsena izmena");
            txtKolicina.setText("");
        }
        }
        popuniTabelu();
    }//GEN-LAST:event_btnIzmeniActionPerformed

